/****************************************************************************************/
/*	[モジュール名]	PWMDrv_SetDutyCycle		[名称]	周期/DUTY設定処理					*/
/*======================================================================================*/
/*	[処理概要]	引数で指定されたチャネルに対し、指定された周期、DUTYのPPG出力を行う。	*/
/*======================================================================================*/
/*	[記述形式]	SI_8 PWMDrv_SetDutyCycle(E_PWM_DRV_PPG_CH ch,UI_16 cycle,UI_16 duty)	*/
/*																						*/
/*--------------------------------------------------------------------------------------*/
/*	[ 引  数 ]	E_PWM_DRV_PPG_CH		ch		：PPGチャネル							*/
/*				UI_16					cycle	：周期設定値							*/
/*				UI_16					duty	：Duty設定値							*/
/*--------------------------------------------------------------------------------------*/
/*	[ 戻り値 ]	SI_8		rc_out	: 正常(D_OK)、異常(D_NG)を返す						*/
/*======================================================================================*/
/*	[ 備  考 ]																			*/
/*	・周期が1以下、または周期よりもDutyの値が大きい場合は異常な設定と判断して、			*/
/*    停止・消灯させる																	*/
/*  ・100％出力可能																		*/
/*	・0％(Duty=0)を指定した場合、出力マスクに1を入れて0％出力設定を行い、				*/
/*    停止・消灯させる																	*/
/*	・チャネル間違い(存在しないチャネルを選択)は処理を行わず終了する					*/
/****************************************************************************************/
SI_8 PWMDrv_SetDutyCycle( E_PWM_DRV_PPG_CH ch, UI_16 cycle, UI_16 duty )
{
	SI_8  rc_out;           /* 戻り値 */
	UI_16 pcn_val;          /* PPG制御ステータスレジスタに設定する値 */
	UI_16 pcn_crr;         	/* PPG制御ステータスレジスタの現在の設定値(レジスタから値を設定) */

	pcn_val = PCN_SET_VALUE;			
	
	/* チャネル正常 */	
	if ( ch < (UI_8)PPG_CH_NUMBER ) {
		
		/* 周期、Dutyが正常範囲内である */
		if ( ( cycle > CYCLE_JUDGMENT ) && ( cycle >= duty ) ) {
			pcn_val=C_PcnSettingTbl[ch];
			
			/* Duty=0%の場合 */
			if ( duty == DUTY_JUDGMENT ) {
				/* PPG制御ステータスレジスタ 固定出力（出力マスク）時の起動設定  */
				pcn_val |= PCN_RUN_WITH_MASK;
			} else {
				/* PPG制御ステータスレジスタ PWM（出力マスク無し）時の起動設定 */
				pcn_val |= PCN_RUN_WITH_PWM;
			}
			
		}
		
		/* 現在のレジスタ設定値を格納設定値 */
		pcn_crr = *C_PcnReg[ch];
		/* 有効ビットマスク設定(タイマ動作許可、ソフトウェアトリガ、カウンタクロック選択、PPG 出力マスク選択、割込み要因選択、PPG 出力極性) */
		pcn_crr &= PCN_USED_BIT_MASK;
		/* PPG制御ステータスレジスタ 固定出力（出力マスク）時の起動設定 */
		pcn_crr |= PCN_RUN_WITH_PWM;
			
		if ( ( pcn_val != pcn_crr ) || ( pcn_val == PCN_SET_VALUE ) ) {
			/* PPG制御ステータスレジスタ停止設定 */
			*C_PcnReg[ch] = C_PcnCtrlStopRegTbl[ch];
			/* PPG周期設定レジスタ停止設定 */
			*C_PcsrReg[ch] = C_PcsrRegStopTbl[ch];
			/* PPGデューティ設定レジスタ */
			*C_PdutReg[ch] = C_PdutRegStopTbl[ch];
		}
		
		/* Output enabled? */
		if ( pcn_val != PCN_SET_VALUE ) {
			*C_PcsrReg[ch] = ( cycle - PWM_SUBTRACTION );
			
			if ( duty > DUTY_JUDGMENT ) {
				*C_PdutReg[ch] = ( duty - PWM_SUBTRACTION );
			} else {
				*C_PdutReg[ch] = DUTY_CLEA;
			}
			
			*C_PcnReg[ch] = pcn_val;
			rc_out = D_OK;
			
		} else {
			rc_out = D_NG;
		}
		
		
	} else {
		rc_out = D_NG;
	}
	return rc_out;
}

/****************************************************************************************/
/*	[モジュール名]	PWMDrv_SetDutyCycleSync	[名称]	周期/DUTY設定処理(同期)				*/
/*======================================================================================*/
/*	[処理概要]	引数で指定された2つのチャネルに対し、指定された周期、					*/
/*              DUTYのPPG出力を同期させて行う。											*/
/*======================================================================================*/
/*	[記述形式]	UI_8 PWMDrv_SetDutyCycleSync(UI_8 ch1,UI_16 cycle,						*/
/*	                                         UI_16 duty1,UI_8 ch2,UI_16 duty2)			*/
/*--------------------------------------------------------------------------------------*/
/*	[ 引  数 ]	E_PWM_DRV_PPG_CH	ch1		：PPGチャネル								*/
/*				UI_16				cycle	：周期設定値								*/
/*				UI_16				duty1	：Duty設定値								*/
/*				E_PWM_DRV_PPG_CH	ch2	    ：PPGチャネル								*/
/*				UI_16				duty2	：Duty設定									*/
/*--------------------------------------------------------------------------------------*/
/*	[ 戻り値 ]	SI_8		rc_out	: 正常(D_OK)、異常(D_NG)を返す						*/
/*======================================================================================*/
/*	[ 備  考 ]																			*/
/*																						*/
/****************************************************************************************/
SI_8 PWMDrv_SetDutyCycleSync( E_PWM_DRV_PPG_CH ch1, UI_16 cycle, UI_16 duty1, E_PWM_DRV_PPG_CH ch2, UI_16 duty2)
{
	SI_8  rc_out;
	UI_16 mask_level;
	UI_16 pcn_val1;
	UI_16 pcn_val2;
	
	/* チャネル指定が正常 */
	if ( (ch1 < PPG_CH_NUMBER) && (ch2 < PPG_CH_NUMBER) && (ch1 != ch2) ) {
		rc_out = D_OK;
		if ((cycle > 1) && (cycle >= duty1) && (cycle >= duty2) && (duty1 > 0) && (duty2 > 0)) {
			/* PPG制御ステータスレジスタ:カウント開始/トリガONを設定 */
			pcn_val1 = C_PcnSettingActTbl[ch1] | PCN_RUN_WITH_PWM;
			pcn_val2 = C_PcnSettingActTbl[ch2] | PCN_RUN_WITH_PWM;
			
			/* 位相差を最小にするために、割込み禁止設定を行う */
			(void)IntrDrv_GetMaskLevel( &mask_level );				/* 割り込みマスクレベルの取得 */
			(void)IntrDrv_SetMaskLevel( INTR_DISABLE_LEVEL );		/* 割り込みマスクレベルの設定 */
			
			/* ch1 周期/デューティの設定 */
			*C_PcsrReg[ch1] = ( cycle - PWM_SUBTRACTION );
			*C_PdutReg[ch1] = ( duty1 - PWM_SUBTRACTION );
			
			/* ch2 周期/デューティの設定 */
			*C_PcsrReg[ch2] = ( cycle - PWM_SUBTRACTION );
			*C_PdutReg[ch2] = ( duty2 - PWM_SUBTRACTION );
			
			/* PPG制御ステータスレジスタを設定 */
			*C_PcnReg[ch1] = pcn_val1;
			*C_PcnReg[ch2] = pcn_val2;
			
			(void)IntrDrv_SetMaskLevel( mask_level );				/* 割り込みマスクレベルの設定 */
			
		} else {
			
			*C_PcnReg[ch1] = C_PcnSettingStopTbl[ch1];
			*C_PcnReg[ch2] = C_PcnSettingStopTbl[ch2];
			
			/* ch1 周期/デューティの設定 */
			*C_PcsrReg[ch1] = 0;
			*C_PdutReg[ch1] = 0;
			
			/* ch2 周期/デューティの設定 */
			*C_PcsrReg[ch2] = 0;
			*C_PdutReg[ch2] = 0;
			
		}
	} else {
		rc_out = D_NG;
	}
	
	
	return rc_out;
}

