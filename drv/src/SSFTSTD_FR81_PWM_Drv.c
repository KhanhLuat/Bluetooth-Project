/*	[ 戻り値 ]	SI_8		rc_out	: 正常(D_OK)、異常(D_NG)を返す						*/
/*======================================================================================*/
/*	[ 備  考 ]																			*/
/*		チャネル間違い(存在しないチャネルを選択)は処理を行わず終了する					*/
/****************************************************************************************/
SI_8 PWMDrv_ReqIntrEnable( E_PWM_DRV_PPG_CH  ch )
{
	SI_8  rc_out;   /* 戻り値 */
	UI_16 pcn_work; /* PPG制御ステータスレジスタ演算用work変数 */
	
	/* チャネル正常の場合 */
	if ( ch < ( UI_8 )PPG_CH_NUMBER ) {
		pcn_work = *C_PcnReg[ch];
		pcn_work |= PCN_IREN_SET;
		*C_PcnReg[ch] = pcn_work;
		rc_out = D_OK;
	} else {
	/* チャネル異常の場合 */
		rc_out = D_NG;
	}
	
	return rc_out;
}


/****************************************************************************************/
/*	[モジュール名]	PWM_Drv_Init		[名称]PWMドライバレジスタ初期設定処理			*/
/*======================================================================================*/
/*	[処理概要]	レジスタ初期設定時に本関数を呼び、初期化を行う。						*/
/*======================================================================================*/
/*	[記述形式]	void PWM_Drv_Init( E_INIT_TYPE req  )									*/
/*--------------------------------------------------------------------------------------*/
/*	[ 引  数 ]	E_INIT_TYPE				req		：起動種別								*/
/*--------------------------------------------------------------------------------------*/
/*	[ 戻り値 ]	無し																	*/
/*======================================================================================*/
/*	[ 備  考 ]	無し																	*/
/****************************************************************************************/
void PWMDrv_Init( E_INIT_TYPE req )
{
	UI_8	ch;	/* PPGチャネル番号 */
	
	switch ( req ) {
	case E_INIT_RESET:			/* CPUリセットによる起動 */
	case E_INIT_WAKEUP:			/* 省電力解除（WAKEUP）による起動 */
		
		/* PPG周期設定レジスタ,PPGデューティ設定レジスタ,PPG制御ステータスレジスタ 初期設定 */
		for ( ch = 0; ch < PPG_CH_NUMBER; ch++ ) {
			*C_PcsrReg[ch] = C_PcsrSettingTbl[ch];
			*C_PdutReg[ch] = C_PdutSettingTbl[ch];
			*C_PcnReg[ch] = C_PcnSettingTbl[ch];
		}
		
		PPGDIV = INIT_PPGDIV;	/* PPG0出力分周設定レジスタ 初期設定 */
		
		GCN10 = INIT_GCN10;	/* ジェネラル制御レジスタ10 初期設定 */
		GCN11 = INIT_GCN11;	/* ジェネラル制御レジスタ11 初期設定 */
		GCN12 = INIT_GCN12;	/* ジェネラル制御レジスタ12 初期設定 */
		GCN13 = INIT_GCN13;	/* ジェネラル制御レジスタ13 初期設定 */
		GCN14 = INIT_GCN14;	/* ジェネラル制御レジスタ14 初期設定 */
		GCN15 = INIT_GCN15;	/* ジェネラル制御レジスタ15 初期設定 */
		GCN20 = INIT_GCN20;	/* ジェネラル制御レジスタ20 初期設定 */
		GCN21 = INIT_GCN21;	/* ジェネラル制御レジスタ21 初期設定 */
		GCN22 = INIT_GCN22;	/* ジェネラル制御レジスタ22 初期設定 */
		GCN23 = INIT_GCN23;	/* ジェネラル制御レジスタ23 初期設定 */
		GCN24 = INIT_GCN24;	/* ジェネラル制御レジスタ24 初期設定 */
		GCN25 = INIT_GCN25;	/* ジェネラル制御レジスタ25 初期設定 */
		
		break;
	case E_INIT_IGN_ON:			/* IGN ON */
		break;
	case E_INIT_RET_NORMAL_VOL:	/* 低電圧復帰 */
		break;
	case E_INIT_INTERVAL_WAKEUP:	/* 間欠起動ウェイクアップ */
		break;
	default:					/* 上記以外 */
		break;
	}
	
}

