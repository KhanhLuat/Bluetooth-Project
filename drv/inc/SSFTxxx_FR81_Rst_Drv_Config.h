#define RST_RSTRR_SRST		(0x00000001UL)		/* ソフトウェアリセット */
#define RST_RSTRR_SCRT		(0x00000002UL)		/* フラッシュセキュリティ違反リセット */
#define RST_RSTRR_WDG0		(0x00000010UL)		/* ウォッチドッグタイマ0リセット */
#define RST_RSTRR_WDG1		(0x00000020UL)		/* ウォッチドッグタイマ1リセット */
#define RST_RSTRR_ERST		(0x00000040UL)		/* RSTX端子リセット検出、不正スタンバイモード移行検出、外部低電圧検出 					*/
												/* クロックスーパバイザリセット検出、またはRSTX外部端子とNMI外部端子の同時アサート検出 	*/
#define RST_RSTRR_IRRST		(0x00000080UL)		/* イレギュラーリセット */
#define RST_CPUAR_HWDF		(0x00000100UL)		/* ハードウェアウォッチドッグ検出フラグ */
#define RST_CPUAR_PSTF		(0x00000200UL)		/* 不正スタンバイモード移行検出フラグ */
#define RST_CPUAR_PMDF		(0x00000400UL)		/* PLLモードメイン発振断検出フラグ */
#define RST_PMUSTR_RSTX_F	(0x00010000UL)		/* RSTX入力リセット */
#define RST_PMUSTR_PONR_F	(0x00020000UL)		/* パワーオンリセット */
#define RST_PMUSTR_PMUST	(0x00800000UL)		/* 電源遮断からの復帰 */
#define RST_LVD_LVD_F		(0x01000000UL)		/* 内部低電圧リセット */
#define RST_LVD5F_LVD5F_F	(0x02000000UL)		/* 外部低電圧リセット */
#define RST_CSVCR_MM		(0x04000000UL)		/* クロックスーパバイザリセット */

#endif	/*	__MB91570_RST_DRV_CONFIG_H__ 	*/



