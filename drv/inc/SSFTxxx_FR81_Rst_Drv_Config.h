/*==============================================================================*/
/*	define定義（機種依存）														*/
/*==============================================================================*/
#define INIT_RSTCR			(0xE0U)	/*	リセット制御レジスタ */
									/*	<07-05>	RDLY	: 111	(リセット発行遅延ビット PCLK × 256サイクル)	*/
									/*	<04-01>	-		: 0000	(予約bit)	*/
									/*	<00>	SRST	: 0		(ソフトウェアリセット 出力しない)	*/
									/* ------------------------------------------------------------------- */

#define INIT_CPUAR_PSTRE	D_OFF	/*	不正スタンバイモード移行検出リセット許可ビット リセットを発生しない */
#define	INIT_CPUAR_PMDF		D_OFF	/*	PLLモードメイン発振断検出フラグ */
#define	INIT_CPUAR_PSTF		D_OFF	/*	不正スタンバイモード移行検出フラグ */
#define INIT_CPUAR_HWDF		D_OFF	/*	ハードウェアウォッチドッグ検出フラグ */

#define INIT_PMUSTR			(0x00U)	/*	PMUステータスレジスタ */
									/* 必ず初期化処理でフラグをクリアすること */
									/*	<07>	PMUST	: 0		(PMUステータスビット 初期状態、初期化リセットからの動作復帰)	*/
									/*	<06-02>	-		: 0		(予約bit)	*/
									/*	<01>	PONR_F	: 0		(パワーオンリセットビット 検出していない)	*/
									/*	<00>	RSTX_F	: 0		(RSTX入力リセットビット 検出していない)		*/
									/* ------------------------------------------------------------------- */
#define INIT_CSVCR			(0x10U)	/* クロックスーパバイザ制御レジスタ */
									/* 		以下の設定値のみ使用可能										*/
									/* 		0x00 : スタンバイ中のCR発信禁止 且つ クロックスーパバイザ禁止	*/
									/* 		0x10 : スタンバイ中のCR発信許可 且つ クロックスーパバイザ禁止	*/
									/* 		0x18 : スタンバイ中のCR発信許可 且つ クロックスーパバイザ許可	*/
									/* <07>		-		: 0			(予約bit) */
									/* <06>		MM		: 0			(メインクロック停止ビット メインクロック復旧時、本ビットをクリア) */
									/* <05>		SM		: 0			(サブクロック停止ビット サブクロック復旧時、本ビットをクリア) */
									/* <04>		RCE		: 1			(CR発振許可ビット STBYモード時のCR発振許可（初期値)) */
									/* <03>		MSVE	: 0			(メインクロックスーパバイザ許可ビット メインクロックスーパバイザ禁止) */
									/* <02>		SSVE	: 0			(サブクロックスーパバイザ許可ビット サブクロックスーパバイザ禁止) */
									/* <01>		SRST	: 0			(サブクロックモード・リセットビット サブクロック断状態でメインクロックからサブクロックに切替えたときにリセット発行しない) */
									/* <00>		-		: 0			(予約bit) */
									/* ------------------------------------------------------------------- */
#define INIT_LVD			(0xC0U)		/* 内部低電圧検出レジスタ */
									/* <07>		LVD_PD	: 1			(内部電圧立下りパワーダウンビット 内部電圧立下りパワーダウン無効) */
									/* <06-04>	LVD_SEL	: 100		(内部立下り検出電圧設定ビット 0.9V±0.1V) */
									/* <03>		LVD_OE	: 0			(内部電圧立下り検出出力許可設定ビット 許可) */
									/* <02-01>	-		: 00		(予約bit) */
									/* <00>		LVD_F	: 0			(内部電圧立下り検出フラグビット フラグをクリア) */
									/* ------------------------------------------------------------------- */
#define INIT_LVD5R			(0x00U)		/* 外部低電圧検出立上り検出レジスタ */
									/* <07-01>	-		: 0			(予約bit) */
									/* <00>		LVD5R_F	: 0			(外部電圧立上り検出フラグビット フラグをクリア) */
									/* ------------------------------------------------------------------- */
#define INIT_LVD5F			(0xA0U)		/* 外部低電圧検出立下り検出レジスタ */
									/* <07>		LVD5F_PD: 1			(外部電圧立下りパワーダウン設定ビット 外部電圧立下りパワーダウン無効) */
									/* <06>		-		: 0			(予約bit) */
									/* <05-04>	LVD5F_SEL: 10		(外部立下り検出電圧設定ビット 4.1V±0.2V) */
									/* <03>		LVD5F_OE: 0			(外部電圧立下り検出出力許可設定ビット 許可) */
									/* <02-01>	-		: 00		(予約bit) */
									/* <00>		LVD5F_F	: 0			(外部電圧立下り検出フラグビット フラグをクリア) */
									/* ------------------------------------------------------------------- */

#define STOP_CSVCR			(0x10U)		/* INIT_CSVCRと同じ設定値 */
#define STOP_LVD			(0xC0U)		/* INIT_LVDと同じ設定値 */
#define STOP_LVD5R			(0x00U)		/* INIT_LVD5Rと同じ設定値 */
#define STOP_LVD5F			(0xA0U)		/* INIT_LVD5Fと同じ設定値 */

#endif								/* __MB91570_RST_DRV_INTERNAL__ */

